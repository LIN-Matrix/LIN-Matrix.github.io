SOCKET: 创建一个新的套接字，用于进程间通信或网络通信。
SOCKETPAIR: 创建一对相关的套接字，用于在同一主机上的进程间双向通信。
BIND: 将套接字绑定到一个特定的地址和端口上，使其能够接收来自该地址和端口的连接或数据。
LISTEN: 将套接字置于监听状态，准备接受传入的连接请求。通常用于服务器端套接字。
ACCEPT: 接受一个传入的连接请求，并创建一个新的套接字用于与客户端进行通信。
CONNECT: 主动连接到一个远程地址和端口，用于客户端套接字发起连接。
GETSOCKNAME: 获取套接字的本地地址信息，包括IP地址和端口号。
GETPEERNAME: 获取与套接字连接的远程地址信息，包括对方的IP地址和端口号。
SENDTO: 通过套接字发送数据到指定的目标地址，常用于无连接的通信（如UDP）。
RECVFROM: 从套接字接收数据，并获取发送方的地址信息，常用于无连接的通信（如UDP）。
SETSOCKOPT: 设置套接字的选项参数，如缓冲区大小、超时、是否启用Nagle算法等，以调整套接字的行为。
GETSOCKOPT: 获取套接字的选项参数，查询当前套接字的配置状态。
SHUTDOWN: 关闭套接字的发送或接收功能，或同时关闭两者，用于优雅地终止连接。
SENDMSG: 通过套接字发送一条消息，支持发送复杂的数据结构，如包含控制信息的消息。
ACCEPT4: 类似于ACCEPT，但可以在接受连接时同时设置套接字的标志，如非阻塞模式或关闭执行标志，提供更高效的连接处理。
SENDMMSG: 通过套接字批量发送多条消息，提高数据发送的效率，适用于高性能网络应用。

